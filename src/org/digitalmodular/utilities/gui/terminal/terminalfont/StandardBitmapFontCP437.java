	public static final AbstractTerminalFont[] fonts = new AbstractTerminalFont[3];

	private StandardBitmapFontCP437(int gridSpacingX, int gridSpacingY) {
		this.gridSpacingX = gridSpacingX;
		this.gridSpacingY = gridSpacingY;
	}

	public static AbstractTerminalFont getFont(int height) {
		int which;
		int offset;
		if (height == 16) {
			which = 0;
			offset = 0;
		} else if (height == 14) {
			which = 1;
			offset = 32;
		} else if (height == 8) {
			which = 2;
			offset = 60;
		} else {
			throw new IllegalArgumentException("Only char heights of 8, 14 and 16 supported.");
		}

		if (fonts[which] == null) {
			fonts[which] = new StandardBitmapFontCP437(8, height);
			for (int i = 0; i < 256; i++) {
				BinaryGlyph glyph = new BinaryGlyph(8, height);
				for (int v = 0; v < height; v++) {
					int c = Integer.parseInt(RAW_DATA[i].substring(offset + v * 2, offset + (v + 1) * 2), 16);
					for (int u = 0; u < 8; u++) {
						glyph.grid[v][u] = (c & 0x80) != 0;
						c <<= 1;
					}
				}
				fonts[which].setGlyph(i, glyph);
			}
		}

		return fonts[which];
	}
